<p><strong>सर्वात महत्त्वाचे:</strong> घरात योग्य 'अर्थिंग' (Grounding) असल्याशिवाय एसी किंवा गीझर चालवणे म्हणजे मृत्यूला आमंत्रण आहे. यामुळे विजेचा धक्का (Shock) लागू शकतो.</p>

